#include "push_swap_bonus.h"

int	run(t_deque *dq, int c)
{
	if (c == 1 || c == 3)
		s(&dq[0]);
	if (c == 2 || c == 3)
		s(&dq[1]);
	if (c == 4)
		p(&dq[0], &dq[1]);
	if (c == 5)
		p(&dq[1], &dq[0]);
	if (c == 6 || c == 8)
		r(&dq[0]);
	if (c == 7 || c == 8)
		r(&dq[1]);
	if (c == 9 || c == 11)
		rr(&dq[0]);
	if (c == 10 || c == 11)
		rr(&dq[1]);
	return (c);
}
